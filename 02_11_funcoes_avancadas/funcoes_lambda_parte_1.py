# Crie uma função lambda chamada multiply que recebe três argumentos, x, y, e z, e retorna o produto deles.
# Após definir a função lambda, chame-a com os valores 2, 3, e 4, e imprima o resultado.

# Create a lambda function that multiplies three numbers
multiply = lambda x, y, z: x * y * z

# Call the lambda function with the values 2, 3, and 4
result = multiply(2, 3, 4)

# Print the result
print(result)